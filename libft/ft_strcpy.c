#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	char	*dst;
	size_t	i;

	dst = (char *)dest;
	i = 0;
	while ((dst[i] = src[i]) != 0)
		i++;
	return (dest);
}
